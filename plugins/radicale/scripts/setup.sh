#!/usr/bin/env bash
set -euo pipefail

CONFIG_DIR="${XDG_CONFIG_HOME:-$HOME/.config}/lab-radicale"
CONFIG_FILE="$CONFIG_DIR/config.env"

sq() {
  printf "'%s'" "$(printf '%s' "$1" | sed "s/'/'\\\\''/g")"
}

write_var() {
  local key="$1"
  local value="$2"
  [[ -z "$value" ]] && return 0
  if [[ "$value" == *$'\n'* || "$value" == *$'\r'* ]]; then
    echo "radicale: refusing to write ${key}; value contains a newline" >&2
    exit 1
  fi
  printf '%s=%s\n' "$key" "$(sq "$value")"
}

if [[ -f "$HOME/.lab/.env" ]]; then
  set -a
  # shellcheck source=/dev/null
  source "$HOME/.lab/.env"
  set +a
fi

url="${CLAUDE_PLUGIN_OPTION_RADICALE_URL:-${RADICALE_URL:-}}"
username="${CLAUDE_PLUGIN_OPTION_RADICALE_USERNAME:-${RADICALE_USERNAME:-}}"
password="${CLAUDE_PLUGIN_OPTION_RADICALE_PASSWORD:-${RADICALE_PASSWORD:-}}"

mkdir -p "$CONFIG_DIR"
chmod 700 "$CONFIG_DIR" 2>/dev/null || true

umask 177
{
  echo "# Generated by the Radicale plugin hook. Do not edit by hand."
  write_var "RADICALE_URL" "${url%/}"
  write_var "RADICALE_USERNAME" "$username"
  write_var "RADICALE_PASSWORD" "$password"
} > "$CONFIG_FILE"
chmod 600 "$CONFIG_FILE" 2>/dev/null || true

if [[ -z "$url" || -z "$username" || -z "$password" ]]; then
  echo "radicale: config written, but URL, username, or password is not configured yet" >&2
  exit 0
fi

echo "radicale: config written to $CONFIG_FILE"
