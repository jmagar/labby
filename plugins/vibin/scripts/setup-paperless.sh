#!/usr/bin/env bash
set -euo pipefail

CONFIG_DIR="${XDG_CONFIG_HOME:-$HOME/.config}/lab-paperless"
CONFIG_FILE="$CONFIG_DIR/config.env"

sq() {
  printf "'%s'" "$(printf '%s' "$1" | sed "s/'/'\\\\''/g")"
}

write_var() {
  local key="$1"
  local value="$2"
  [[ -z "$value" ]] && return 0
  if [[ "$value" == *$'\n'* || "$value" == *$'\r'* ]]; then
    echo "paperless: refusing to write ${key}; value contains a newline" >&2
    exit 1
  fi
  printf '%s=%s\n' "$key" "$(sq "$value")"
}

if [[ -f "$HOME/.lab/.env" ]]; then
  set -a
  # shellcheck source=/dev/null
  source "$HOME/.lab/.env"
  set +a
fi

url="${CLAUDE_PLUGIN_OPTION_PAPERLESS_URL:-${PAPERLESS_URL:-}}"
token="${CLAUDE_PLUGIN_OPTION_PAPERLESS_API_TOKEN:-${CLAUDE_PLUGIN_OPTION_PAPERLESS_API_KEY:-${CLAUDE_PLUGIN_OPTION_PAPERLESS_TOKEN:-${PAPERLESS_API_TOKEN:-${PAPERLESS_API_KEY:-${PAPERLESS_TOKEN:-}}}}}}"

mkdir -p "$CONFIG_DIR"
chmod 700 "$CONFIG_DIR" 2>/dev/null || true

umask 177
{
  echo "# Generated by the Paperless plugin hook. Do not edit by hand."
  write_var "PAPERLESS_URL" "${url%/}"
  write_var "PAPERLESS_API_TOKEN" "$token"
} > "$CONFIG_FILE"
chmod 600 "$CONFIG_FILE" 2>/dev/null || true

if [[ -z "$url" || -z "$token" ]]; then
  echo "paperless: config written, but URL or API token is not configured yet" >&2
  exit 0
fi

echo "paperless: config written to $CONFIG_FILE"
