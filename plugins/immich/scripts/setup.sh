#!/usr/bin/env bash
set -euo pipefail

CONFIG_DIR="${XDG_CONFIG_HOME:-$HOME/.config}/lab-immich"
CONFIG_FILE="$CONFIG_DIR/config.env"

sq() {
  printf "'%s'" "$(printf '%s' "$1" | sed "s/'/'\\\\''/g")"
}

write_var() {
  local key="$1"
  local value="$2"
  [[ -z "$value" ]] && return 0
  if [[ "$value" == *$'\n'* || "$value" == *$'\r'* ]]; then
    echo "immich: refusing to write ${key}; value contains a newline" >&2
    exit 1
  fi
  printf '%s=%s\n' "$key" "$(sq "$value")"
}

if [[ -f "$HOME/.lab/.env" ]]; then
  set -a
  # shellcheck source=/dev/null
  source "$HOME/.lab/.env"
  set +a
fi

url="${CLAUDE_PLUGIN_OPTION_IMMICH_URL:-${IMMICH_URL:-}}"
api_key="${CLAUDE_PLUGIN_OPTION_IMMICH_API_KEY:-${IMMICH_API_KEY:-}}"

mkdir -p "$CONFIG_DIR"
chmod 700 "$CONFIG_DIR" 2>/dev/null || true

umask 177
{
  echo "# Generated by the Immich plugin hook. Do not edit by hand."
  write_var "IMMICH_URL" "${url%/}"
  write_var "IMMICH_API_KEY" "$api_key"
} > "$CONFIG_FILE"
chmod 600 "$CONFIG_FILE" 2>/dev/null || true

if [[ -z "$url" || -z "$api_key" ]]; then
  echo "immich: config written, but URL or API key is not configured yet" >&2
  exit 0
fi

echo "immich: config written to $CONFIG_FILE"
