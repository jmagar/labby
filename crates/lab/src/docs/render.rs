fn header(name: &str, command: &str) -> String {
    format!(
        "# {}\n\nGenerated by `{}`. Do not edit by hand.\n\n",
        name, command
    )
}

fn write_cli_command(out: &mut String, command: &mut clap::Command, path: &str) {
    writeln!(out, "## `{path}`\n\n```text").ok();
    let help = command.render_long_help().to_string();
    for line in help.trim_end().lines() {
        out.push_str(line.trim_end());
        out.push('\n');
    }
    out.pop();
    out.push_str("\n```\n\n");

    let subcommands = command
        .get_subcommands()
        .map(|subcommand| subcommand.get_name().to_string())
        .collect::<Vec<_>>();
    for name in subcommands {
        if let Some(subcommand) = command.find_subcommand_mut(&name) {
            write_cli_command(out, subcommand, &format!("{path} {name}"));
        }
    }
}

fn write_feature_row(out: &mut String, feature: &FeatureDoc) {
    writeln!(
        out,
        "| {} | {} | {} | {} | {} | {} | {} |",
        cell(&feature.crate_name),
        code(&feature.feature),
        cell(&format!("{:?}", feature.classification)),
        feature.included_in_default,
        feature.included_in_all,
        cell(&opt(feature.mapped_crate_feature.as_deref())),
        feature
            .dependencies
            .iter()
            .map(|dep| code(dep))
            .collect::<Vec<_>>()
            .join("<br>")
    )
    .ok();
}

fn surfaces(surfaces: &SurfaceAvailability) -> String {
    [
        ("cli", surfaces.cli),
        ("mcp", surfaces.mcp),
        ("api", surfaces.api),
        ("web", surfaces.web_ui),
    ]
    .into_iter()
    .filter_map(|(name, enabled)| enabled.then_some(name))
    .collect::<Vec<_>>()
    .join(", ")
}

fn opt(value: Option<&str>) -> String {
    value.unwrap_or("-").to_string()
}

fn cell(value: &str) -> String {
    value
        .replace('&', "&amp;")
        .replace('|', "\\|")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('\n', "<br>")
}

fn code(value: &str) -> String {
    format!("`{}`", cell(&value.replace('`', "\\`")))
}

fn params_cell<P: ParamLike>(params: &[P]) -> String {
    params
        .iter()
        .map(|param| {
            let rendered = if param.required() {
                format!("{}*: {}", param.name(), param.ty())
            } else {
                format!("{}: {}", param.name(), param.ty())
            };
            code(&rendered)
        })
        .collect::<Vec<_>>()
        .join("<br>")
}

trait ParamLike {
    fn name(&self) -> &str;
    fn ty(&self) -> &str;
    fn required(&self) -> bool;
}

impl ParamLike for ParamDoc {
    fn name(&self) -> &str {
        &self.name
    }

    fn ty(&self) -> &str {
        &self.ty
    }

    fn required(&self) -> bool {
        self.required
    }
}

impl ParamLike for ParamEntry {
    fn name(&self) -> &str {
        &self.name
    }

    fn ty(&self) -> &str {
        &self.ty
    }

    fn required(&self) -> bool {
        self.required
    }
}

impl ParamLike for lab_apis::core::action::ParamSpec {
    fn name(&self) -> &str {
        self.name
    }

    fn ty(&self) -> &str {
        self.ty
    }

    fn required(&self) -> bool {
        self.required
    }
}
