pub fn json<T: Serialize>(value: &T) -> anyhow::Result<String> {
    Ok(format!("{}\n", serde_json::to_string_pretty(value)?))
}

pub fn mcp_help(catalog: &crate::catalog::Catalog) -> String {
    let mut out = header("mcp-help", "labby docs generate");
    out.push_str("| Service | Category | Status | Action | Destructive | Params | Returns |\n");
    out.push_str("| --- | --- | --- | --- | --- | --- | --- |\n");
    for service in &catalog.services {
        for action in &service.actions {
            writeln!(
                out,
                "| {} | {} | {} | {} | {} | {} | {} |",
                code(&service.name),
                cell(&service.category),
                cell(&service.status),
                code(&action.name),
                action.destructive,
                params_cell(&action.params),
                code(&action.returns)
            )
            .ok();
        }
    }
    out
}

pub fn cli_help() -> String {
    let mut out = String::from("# Lab CLI Help\n\n");
    out.push_str(
        "Generated by `labby docs generate` from clap command definitions. Do not edit by hand.\n\n",
    );
    let mut command = crate::cli::Cli::command();
    write_cli_command(&mut out, &mut command, "labby");
    while out.ends_with("\n\n") {
        out.pop();
    }
    out
}

pub fn service_catalog(services: &[ServiceDoc]) -> String {
    let mut out = header("service-catalog", "labby docs generate");
    out.push_str("| Service | Status | Exposure | Feature | Category | Surfaces | Metadata |\n");
    out.push_str("| --- | --- | --- | --- | --- | --- | --- |\n");
    for service in services {
        writeln!(
            out,
            "| {} | {} | {} | {} | {} | {} | {} |",
            code(&service.name),
            cell(&service.status),
            cell(&format!("{:?}", service.exposure)),
            cell(&opt(service.feature.as_deref())),
            cell(&service.category),
            cell(&surfaces(&service.surfaces)),
            cell(&service.metadata_source)
        )
        .ok();
    }
    out
}

