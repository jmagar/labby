pub fn feature_matrix(matrix: &FeatureMatrix) -> String {
    let mut out = header("feature-matrix", "labby docs generate");
    if matrix.mismatches.is_empty() {
        out.push_str("Feature invariant status: clean.\n\n");
    } else {
        out.push_str("Feature invariant mismatches:\n\n");
        for mismatch in &matrix.mismatches {
            writeln!(
                out,
                "- {}: {}",
                code(&mismatch.feature),
                cell(&mismatch.message)
            )
            .ok();
        }
        out.push('\n');
    }
    out.push_str("| Crate | Feature | Class | In Default | In All | Maps To | Dependencies |\n");
    out.push_str("| --- | --- | --- | --- | --- | --- | --- |\n");
    for feature in &matrix.features {
        write_feature_row(&mut out, feature);
    }
    out
}

